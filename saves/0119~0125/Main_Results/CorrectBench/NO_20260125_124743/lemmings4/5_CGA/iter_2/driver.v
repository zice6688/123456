// CGA 2
task test_coverage_fix_2;
  input logic clk, areset;
  input logic bump_left, dig, ground;

  // Reset the DUT
  areset = 1'b1;
  repeat(2) @(posedge clk);
  areset = 1'b0;
  @(posedge clk);

  // Drive inputs to trigger the missing FSM transitions
  // Start with the initial state (assumed to be WL)
  ground = 1'b0;  // Ensure not on ground
  bump_left = 1'b0;
  dig = 1'b0;
  repeat(5) @(posedge clk);  // Stay in the initial state for a few cycles

  // Trigger the transition from WL to DIGL
  dig = 1'b1;
  bump_left = 1'b0;
  ground = 1'b0;
  repeat(5) @(posedge clk);  // Stay in DIGL for a few cycles

  // Transition from DIGL to FALLL
  ground = 1'b1;
  dig = 1'b0;
  bump_left = 1'b0;
  repeat(5) @(posedge clk);  // Stay in FALLL for a few cycles

  // Transition from FALLL to DEAD
  ground = 1'b1;
  fall_counter = 20;  // Simulate the fall_counter reaching 20
  repeat(5) @(posedge clk);  // Stay in DEAD for a few cycles

endtask


