// CGA 2
task test_coverage_fix_2;
  input int N; // Number of clock cycles to wait
  begin
    // Drive inputs to trigger the missing logic in the FSM
    // Specifically, we need to reach the state WL and then set dig to 1
    // to transition to DIGL

    // Reset the DUT
    areset = 1;
    repeat(2) @(posedge clk);
    areset = 0;

    // Drive the FSM to the WL state
    ground = 0; // Ensure not in ground
    bump_left = 0; // Ensure not bumping left
    bump_right = 0; // Ensure not bumping right
    dig = 0; // Ensure not digging
    repeat(N) @(posedge clk); // Wait for N clock cycles to ensure the FSM is in WL state

    // Set dig to 1 to transition from WL to DIGL
    dig = 1;
    repeat(N) @(posedge clk); // Wait for N clock cycles to ensure the transition

    // Optionally, you can add more steps to cover other related blocks if needed
    // For example, to cover the transition from DIGL to FALLL:
    ground = 1; // Set ground to 1 to transition from DIGL to FALLL
    repeat(N) @(posedge clk); // Wait for N clock cycles to ensure the transition

    // Reset the inputs to their default values
    ground = 0;
    bump_left = 0;
    bump_right = 0;
    dig = 0;
  end
endtask


