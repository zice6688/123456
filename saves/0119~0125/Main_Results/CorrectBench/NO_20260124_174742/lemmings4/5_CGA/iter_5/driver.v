// CGA 5
task test_coverage_fix_5;
  input int N; // Number of clock cycles to wait
  begin
    // Initial state setup
    areset = 1'b1;
    @(posedge clk);
    areset = 1'b0;

    // Drive inputs to reach the WL state
    ground = 1'b0;
    dig = 1'b0;
    bump_left = 1'b0;
    bump_right = 1'b0;
    repeat(N) @(posedge clk);

    // Ensure we are in the WL state
    if (state != WL) begin
      $display("Error: Expected state WL, but got %s", state.name());
      return;
    end

    // Drive inputs to trigger the missing logic in WL state
    ground = 1'b0;
    dig = 1'b1; // Trigger the DIGL transition
    bump_left = 1'b0;
    bump_right = 1'b0;
    repeat(N) @(posedge clk);

    // Check if the next state is DIGL
    if (next != DIGL) begin
      $display("Error: Expected next state DIGL, but got %s", next.name());
    end else begin
      $display("Success: Triggered the missing logic for DIGL transition");
    end

    // Reset the DUT
    areset = 1'b1;
    @(posedge clk);
    areset = 1'b0;
  end
endtask


